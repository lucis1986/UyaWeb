            <h2>特点</h2>
            <p>为何要独立设置ESD系统呢？当然一般安全联锁保护功能也可由DCS来实现。但是对于较大规模的紧急停车系统应按照安全独立原则与DCS分开设置，这样做主要有以下几方面原因：</p>
            <p>（1）降低控制功能和安全功能同时失效的概率，当维护DCS部分故障时也不会危及安全保护系统;              </p>
            <p>（2）对于大型装置或旋转机械设备而言，紧急停车系统响应速度越快越好。这有利于保护设备，避免事故扩大；并有利于分辨事故原因记录。而DCS处理大量过程监测信息，因此其响应速度难以作得很快；              </p>
            <p>（3）DCS系统是过程控制系统，是动态的，需要人工频繁的干预，这有可能引起人为误动作；而ESD是静态的，不需要人为干预，这样设置ESD可以避免人为误动作。</p>
            <hr />
            <h2>解决方案</h2>
            <p>ESD系统广泛应用于石油化工IIAnews.com，冶金，电力，轻工，纺织，铁路，压缩机，鼓风机等需要安全保护及自动连锁保护的场合和。针对这种场合，GE FANUC AUTOMATION推出了90-70 GMR（Genius Module Redundancy）表决系统。</p>
            <p>　　根据对冗余的不同需求；传感器信号被引入到隔离Genius输入模块。设置在独立机架中工业自动化网版权所有，各自独立的CPU从每条Genius总线上接受这些输入信号并对信号进行表决，然后，根据表决的结果执行相应的应用程序。由于系统的硬件采用了物理无藕合设计和分离式结构电路保护，因此用户不用担心出现硬件故障影响操作。离散输出电路对电流，电压传感器的信号进行综合分析，从而，实现对输出和负载状态的诊断。
            </p>
            <p>　GMR识别系统故障并对其进行自动补偿MMSonline.com.cn，同时，允许在不中断系统运行的情况下对其实施维修和部件更换。故障是由一个软件报警器功能来处理的，它可提供时间记录和I/O故障报告，系统故障的两个诊断表可以由编程器显示，也可以送到计算机或其他协处理器进行显示。每一个PLC中的诊断程序都在持续地运行以检测明显的和隐藏的故障，从而，降低系统的平均修复时间（MTTR）SooQ.cn，同时，为维修和操作人员自动生成故障报告。 </p>
