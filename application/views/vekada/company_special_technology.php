                    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;随着航运业和海洋开发的空前发展，海洋环境的污染也越来越严重，人们对海洋环境的保护也日益重视。因此船用生活污水的处理处理装置在船舶和海上设施的安装和使用显得异常重要。</p>
                    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;我公司生产制作的船用污水处理装置采用电解法处理船用生活污水。电解法船用生活污水处理装置通过独特结构的电解槽，将混有海水的舰船上黑水(厕所污水)和灰水(厨房水和洗澡间水)直接电解，利用电解所产生的次氯酸活性消毒剂杀菌，效率几乎达到100%；同时，将污水中的有机物在电极间发生氧化分解，转变成水和二氧化碳，氧化分解率可达到90%~95%，从而达到排放标。</p>
                    <img class="imgl" src="/tone/vekada/banner/2.jpg" alt="" width="125" height="125" />
                    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;我公司生产的船用污水处理装置采用模块化设计，整机体积小，占地面积小，运行和空载时的重量小。整套装置可以分解安装，适应性强。</p>
                    <p>处理生活污水的种类全面，黑水和灰水都可以处理。</p>
                    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;采用先进的PLC控制工艺和人机界面一体化操作系统，智能管理自动化控制。系统可以自动连续运行，也可以随时启动和停止，操作和维护简单，无需要专业技术人员。
                        高度集成的自动化控制，实现智能的一键式操作；具有多种通讯方式及通讯协议，实现数据远传功能，可实现遥控、遥测功能。
                    </p>
