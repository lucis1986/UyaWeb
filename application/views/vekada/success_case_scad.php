            <h2>通信</h2>

            <p>SCADA系统中的通信分为内部通信、与I/O设备通信、和外界通信。客户与服务器间以及服务器与服务器间一般有三种通信形式，请求式，订阅式与广播式。设备驱动程序与I/O设备通讯一般采用请求式，大多数设备都支持这种通讯方式，当然也有的设备支持主动发送方式。SCADA通过多种方式与外界通信。  SCADA通讯结构
                如OPC，一般都会提供OPC客户端，用来与设备厂家提供的OPC服务器进行通讯。因为OPC有微软内定的标准，所以OPC客户端无需修改就可以与各家提供的OPC服务器进行通讯。
            </p>

