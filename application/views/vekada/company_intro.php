            <h2>企业简介</h2>

            <p><strong>天津开发区威克达科技发展有限公司Vekada® </strong>
                是位于天津经济技术开发区核心区的一家高科技型民营企业，以工业安全产品销售、自动化系统集成、ERP／MES项目实施和软件服务外包服务为主营业务,
                致力为石油、化工、制药、冶金、电力、天然气、海洋油气开采及环境保等领域的广大客户提供国际一流的工业级安全产品与相关服务，并提供全方位工业自动化与企业级信息集成解决方案。</p>

