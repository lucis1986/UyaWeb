            <h2>硬件结构</h2>
            <p>通常SCADA系统分为两个层面，即客户/服务器体系结构。服务器与硬件设备通信，进行数据处理和运算。而客户用于人机交互，如用文字、动画显示现场的状态，并可以对现场的开关、阀门进行操作。近年来又出现一个层面，通过Web发布在Internet上进行监控，可以认为这是一种“超远程客户”。硬件设备(如PLC)一般既可以通过点到点方式连接，也可以以总线方式连接到服务器上。点到点连接一般通过串口(RS232)，总线方式可以是RS485，以太网等连接方式。
            </p>
            <p>&nbsp;</p>

            <hr />

            <h2>软件体系结构</h2>

            <p>SCADA由很多任务组成，每个任务完成特定的功能。位于一个或多个机器上的服务器负责数据采集，数据处理（如量程转换、滤波、报警检查、计算、事件记录、历史存储、执行用户脚本等）  典型的SCADA硬件配置图
                。服务器间可以相互通讯。有些系统将服务器进一步单独划分成若干专门服务器，如报警服务器，记录服务器，历史服务器，登录服务器等。各服务器逻辑上作为统一整体，但物理上可能放置在不同的机器上。分类划分的好处是可以将多个服务器的各种数据统一管理、分工协作，缺点是效率低，局部故障可能影响整个系统。
            </p>
